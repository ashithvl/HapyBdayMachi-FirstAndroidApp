public class ImagesQuotes {
    private final int quotes;
    private final int images;


    public ImagesQuotes(int quotes, int images) {
        this.quotes = quotes;
        this.images = images;

    }

    public int getQuotes() {

        return quotes;
    }

    public int getImages() {

        return images;
    }
}
